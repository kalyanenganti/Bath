package com.akqa.bath;

public class Bath {
float hot_water_temp;
float cold_water_temp;
public float getHot_water_temp() {
	return hot_water_temp;
}
public void setHot_water_temp(float hot_water_temp) {
	this.hot_water_temp = hot_water_temp;
}
public float getCold_water_temp() {
	return cold_water_temp;
}
public void setCold_water_temp(float cold_water_temp) {
	this.cold_water_temp = cold_water_temp;
}

}
